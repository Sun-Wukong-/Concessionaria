public class VisaoProduto {
    
    private long códigoProduto; 
    private String corProduto;
    private String marcaProduto;
    private String modeloProduto;
    private int anoProduto;
    private float preçoProduto;

    /**
     * @return the códigoProduto
     */
    public long getCódigoProduto() {
        return códigoProduto;
    }

    /**
     * @param códigoProduto the códigoProduto to set
     */
    public void setCódigoProduto(long códigoProduto) {
        this.códigoProduto = códigoProduto;
    }

    /**
     * @return the corProduto
     */
    public String getCorProduto() {
        return corProduto;
    }

    /**
     * @param corProduto the corProduto to set
     */
    public void setCorProduto(String corProduto) {
        this.corProduto = corProduto;
    }

    /**
     * @return the marcaProduto
     */
    public String getMarcaProduto() {
        return marcaProduto;
    }

    /**
     * @param marcaProduto the marcaProduto to set
     */
    public void setMarcaProduto(String marcaProduto) {
        this.marcaProduto = marcaProduto;
    }

    /**
     * @return the modeloProduto
     */
    public String getModeloProduto() {
        return modeloProduto;
    }

    /**
     * @param modeloProduto the modeloProduto to set
     */
    public void setModeloProduto(String modeloProduto) {
        this.modeloProduto = modeloProduto;
    }

    /**
     * @return the anoProduto
     */
    public int getAnoProduto() {
        return anoProduto;
    }

    /**
     * @param anoProduto the anoProduto to set
     */
    public void setAnoProduto(int anoProduto) {
        this.anoProduto = anoProduto;
    }

    /**
     * @return the preçoProduto
     */
    public float getPreçoProduto() {
        return preçoProduto;
    }

    /**
     * @param preçoProduto the preçoProduto to set
     */
    public void setPreçoProduto(float preçoProduto) {
        this.preçoProduto = preçoProduto;
    }

    
}
